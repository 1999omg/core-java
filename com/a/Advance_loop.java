package com.a;

public class Advance_loop {

	public class LeftTrianglePattern  
	{    
	public void main(String args[])   
	{    
	int i, j, row = 6;       
	//Outer loop work for rows  
	for (i=0; i<row; i++)   
	{  
	for (j=2*(row-i); j>=0; j--)         
	{  
	System.out.print(" ");   
	}   
	for (j=0; j<=i; j++ )   
	{   
	System.out.print("* ");   
	}   
	System.out.println();   
	}   
	
	int  i1, j1, row1=6;   
	for(i1=0; i1<row1; i1++)   
	{   
	for(j1=0; j1<=i1; j1++)   
	{   
	System.out.print("* ");   
	}   
	System.out.println();   
	
	
	}   
	}
	}
	}
		
		
		
		
	

